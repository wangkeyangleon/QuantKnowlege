#!/bin/bash

# ====================== Git 一键提交脚本 ======================
# 功能：一键 add + commit + push，支持自定义提交信息
# 使用：./git-auto-push.sh "你的提交信息"
# ==============================================================

# 1. 检查是否输入提交信息
if [ $# -eq 0 ]; then
    echo "⚠️  请输入提交信息！"
    echo "✅ 使用示例：./git-auto-push.sh \"修复登录bug，优化页面样式\""
    exit 1
fi

# 2. 合并提交信息（支持多参数）
COMMIT_MSG="$*"

# 3. 执行 Git 命令
echo "========================================"
echo "🚀 开始提交 Git 代码..."
echo "📝 提交信息：$COMMIT_MSG"
echo "========================================"

# 添加所有变更文件
git add .

# 提交代码
git commit -m "$COMMIT_MSG"

# 推送到远程仓库（当前分支）
git push

# 4. 执行结果提示
if [ $? -eq 0 ]; then
    echo "========================================"
    echo "✅ Git 提交推送完成！"
    echo "========================================"
else
    echo "========================================"
    echo "❌ 提交失败，请检查错误信息！"
    echo "========================================"
    exit 1
fi